#include "../push_swap.h"

int	sx(int *tab, int *size_tab)
{
	int	tmp;

	if (*size_tab >= 2)
	{
		tmp = tab[0];
		tab[0] = tab[1];
		tab[1] = tmp;
		return (1);
	}
	return (0);
}
