#include "../push_swap.h"

int	rx(int *tab, int *size_tab)
{
	int	i;
	int	tmp;

	i = *size_tab - 1;
	while (i)
	{
		tmp = tab[i];
		tab[i] = tab[i - 1];
		tab[i - 1] = tmp;
		i--;
	}
	return (1);
}
